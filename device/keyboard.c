void keyboard_handler(void)
{
    uint8_t status;
    uint8_t scancode;

    outb(0x20, 0x20);
    status = inb(0x64);
    if (status & 0x01)
    {
        scancode = inb(0x60);
        char letter = sc_ascii[(int)scancode];
        char str[2] = {letter, '\0'};
        terminal_writestring(str);
    }
}